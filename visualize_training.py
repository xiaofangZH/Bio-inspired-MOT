def extract_step_data(log_path):
    """Extract step-level AvgLoss from training log."""
    pattern = re.compile(
        r"Epoch\s+(\d+),\s+Step\s+(\d+),\s+AvgLoss:\s+([\d.]+)"
    )
    steps = []
    if not Path(log_path).exists():
        return steps
    with open(log_path, 'r', encoding='utf-8') as f:
        for line in f:
            m = pattern.search(line)
            if m:
                steps.append({
                    'epoch': int(m.group(1)),
                    'step': int(m.group(2)),
                    'loss': float(m.group(3)),
                })
    return steps


def extract_epoch_data(log_path):
    """Extract epoch-completion summaries from training log."""
    pattern = re.compile(
        r"Epoch\s+(\d+)\s+完成\s+\|\s+"
        r"loss=([\d.]+)\s+cls=([\d.]+)\s+l1=([\d.]+)\s+ciou=([\d.]+)\s+\|\s+"
        r"P@50=([\d.]+)\s+R@50=([\d.]+)\s+MatchR=([\d.]+)\s+mIoU=([\d.]+)\s+\|\s+"
        r"steps=(\d+)\s+failed=(\d+)\s+skipped_seq=(\d+)\s+\|\s+"
        r"frames=(\d+)/(\d+).*?\|\s+"
        r"([\d.]+)s\s+([\d.]+)\s+step/s\s+([\d.]+)\s+frame/s"
    )
    stage_pattern = re.compile(r"阶段:\s+(\w+)\s+\(Epoch\s+(\d+)-(\d+)")
    epochs = []
    current_stage = "unknown"
    if not Path(log_path).exists():
        return epochs
    with open(log_path, 'r', encoding='utf-8') as f:
        for line in f:
            sm = stage_pattern.search(line)
            if sm:
                current_stage = sm.group(1)
            m = pattern.search(line)
            if m:
                epochs.append({
                    'epoch': int(m.group(1)),
                    'stage': current_stage,
                    'loss_total': float(m.group(2)),
                    'loss_cls': float(m.group(3)),
                    'loss_l1': float(m.group(4)),
                    'loss_ciou': float(m.group(5)),
                    'precision_iou50': float(m.group(6)),
                    'recall_iou50': float(m.group(7)),
                    'match_recall': float(m.group(8)),
                    'mean_matched_iou': float(m.group(9)),
                    'steps': int(m.group(10)),
                    'failed_steps': int(m.group(11)),
                    'skipped': int(m.group(12)),
                    'frames_processed': int(m.group(13)),
                    'frames_expected': int(m.group(14)),
                    'epoch_seconds': float(m.group(15)),
                    'steps_per_second': float(m.group(16)),
                    'frames_per_second': float(m.group(17)),
                })
    return epochs


def load_json_metrics(json_path):
    """Load pre-extracted metrics from JSON."""
    p = Path(json_path)
    if p.exists():
        with open(p) as f:
            return json.load(f)
    return []


