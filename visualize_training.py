# Training report
# ═══════════════════════════════════════════════════════════════════════

def print_report(step_data, epoch_data):
    """Print console training summary."""
    print(f"\n{'='*70}")
    print("  HAMT TRAINING VISUALIZATION REPORT")
    print(f"{'='*70}")

    if step_data:
        losses = [s['loss'] for s in step_data]
        print(f"  Step-level data:  {len(step_data)} steps recorded")
        print(f"  Loss range:       {min(losses):.4f} — {max(losses):.4f}")
        print(f"  Current epoch:    {step_data[-1]['epoch']}")
        print(f"  Current loss:     {step_data[-1]['loss']:.6f}")

    if epoch_data:
        print(f"\n  Epoch-level data: {len(epoch_data)} epochs")
        last = epoch_data[-1]
        print(f"  Final Loss:       {last['loss_total']:.6f}")
        print(f"  Final P@IoU50:    {last['precision_iou50']:.4f}")
        print(f"  Final R@IoU50:    {last['recall_iou50']:.4f}")
        print(f"  Final MatchR:     {last['match_recall']:.4f}")
        print(f"  Final mIoU:       {last['mean_matched_iou']:.4f}")

        # Per-stage summary
        by_stage = defaultdict(list)
        for e in epoch_data:
            by_stage[e.get('stage', 'unknown')].append(e)
        print(f"\n  {'Stage':<10} {'Best P@50':>12} {'Best R@50':>12} {'Best mIoU':>12} {'Min Loss':>12}")
        print(f"  {'-'*60}")
        for sname in sorted(by_stage.keys()):
            sm = by_stage[sname]
            print(f"  {sname:<10} {max(e['precision_iou50'] for e in sm):>12.4f} "
                  f"{max(e['recall_iou50'] for e in sm):>12.4f} "
                  f"{max(e['mean_matched_iou'] for e in sm):>12.4f} "
                  f"{min(e['loss_total'] for e in sm):>12.6f}")


