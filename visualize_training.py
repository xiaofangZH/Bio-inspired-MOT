def main():
    log_path, json_path, eval_files = find_data()

    # --- Data loading ---
    step_data = []
    epoch_data = []

    if log_path:
        print(f"Reading training log: {log_path}")
        step_data = extract_step_data(log_path)
        epoch_data = extract_epoch_data(log_path)
        print(f"  Step records: {len(step_data)}")
        print(f"  Epoch summaries: {len(epoch_data)}")

    # Fallback: if no epoch data from log, try JSON
    if not epoch_data and json_path:
        print(f"Fallback: loading JSON metrics from {json_path}")
        epoch_data = load_json_metrics(json_path)
        print(f"  JSON epoch records: {len(epoch_data)}")

    if not step_data and not epoch_data:
        print("No training data found. Please wait for training to produce output.")
        print(f"Expected log path: {OUTPUT_DIR}/train_v4_*.log")
        return

    OUTPUT_DIR.mkdir(parents=True, exist_ok=True)

    # --- Build figures ---
    print("\nGenerating Nature-quality visualizations...")
    build_figure1(step_data, epoch_data)
    build_figure2(eval_files)
    build_figure3_model_summary()

    # --- Report ---
    print_report(step_data, epoch_data)

    print(f"\nAll figures saved to: {OUTPUT_DIR}")
    print("  fig1_training_dynamics.svg  — Training loss, decomposition, matching metrics")
    print("  fig2_evaluation.svg         — MOT evaluation metrics (post-eval)")
    print("  fig3_model_summary.svg      — Architecture overview")
    print("\nUse SVG for manuscript editing; PNG for quick preview.")


if __name__ == "__main__":
    main()
