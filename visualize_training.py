def _draw_epoch_stage_shading(ax, stage_boundaries):
    """Draw stage background shading on epoch-based x-axis."""
    stage_colors = [PALETTE['stage1_bg'], PALETTE['stage2_bg'], PALETTE['stage3_bg']]
    sorted_stages = sorted(stage_boundaries.keys())
    for idx, sname in enumerate(sorted_stages):
        if idx >= len(stage_colors):
            break
        sr = stage_boundaries[sname]
        ax.axvspan(sr['start'] - 0.5, sr['end'] + 0.5,
                   alpha=0.25, color=stage_colors[idx], zorder=0)
        mid = (sr['start'] + sr['end']) / 2
        y_min, y_max = ax.get_ylim()
        ax.text(mid, y_min + 0.03 * (y_max - y_min), sname,
                ha='center', fontsize=10, color=PALETTE['neutral'],
                style='italic', alpha=0.7)


