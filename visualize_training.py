def _draw_panel_a_loss(ax, step_data, stage_boundaries):
    """Panel a: Step-level loss with EMA smoothing and stage shading."""
    if not step_data:
        ax.text(0.5, 0.5, 'No step-level data yet', ha='center', va='center',
                transform=ax.transAxes, fontsize=14, color=PALETTE['neutral'])
        ax.text(-0.03, 1.06, 'a', transform=ax.transAxes,
                fontsize=22, fontweight='bold', va='top', ha='right')
        ax.spines['bottom'].set_linewidth(2)
        ax.spines['left'].set_linewidth(2)
        ax.set_ylabel('Loss', fontsize=14)
        ax.set_xlabel('Epoch', fontsize=14)
        return

    # Global step index for x-axis
    global_steps = np.arange(1, len(step_data) + 1)
    losses = np.array([s['loss'] for s in step_data], dtype=np.float64)
    epochs = np.array([s['epoch'] for s in step_data])

    # Map epoch numbers to global step range for shading
    epoch_boundaries_steps = {}
    prev_ep = epochs[0]
    for i, ep in enumerate(epochs):
        if ep != prev_ep:
            epoch_boundaries_steps[prev_ep] = global_steps[i - 1]
            prev_ep = ep
    epoch_boundaries_steps[prev_ep] = global_steps[-1]

    # Draw raw loss as faint background
    ax.plot(global_steps, losses, color='#D0D0D0', linewidth=0.5, alpha=0.6, label='Raw loss (per step)')

    # EMA smoothing (alpha=0.05)
    ema = np.zeros_like(losses)
    ema[0] = losses[0]
    for i in range(1, len(losses)):
        ema[i] = 0.05 * losses[i] + 0.95 * ema[i - 1]

    # Label epoch transitions on x-axis
    ep_labels = sorted(epoch_boundaries_steps.keys())
    ax.plot(global_steps, ema, color=PALETTE['loss_smooth'], linewidth=2.2, label='EMA smoothed')

    # Stage shading
    _draw_stage_shading(ax, stage_boundaries, epoch_boundaries_steps)

    ax.set_ylabel('Loss', fontsize=14)
    ax.set_xlabel('Global Step', fontsize=14)
    ax.legend(loc='upper right', fontsize=10, ncol=2)
    ax.set_xlim(0, global_steps[-1])
    ax.text(-0.03, 1.06, 'a', transform=ax.transAxes,
            fontsize=22, fontweight='bold', va='top', ha='right')
    ax.spines['bottom'].set_linewidth(2)
    ax.spines['left'].set_linewidth(2)

    # Add epoch number ticks at top
    if epoch_boundaries_steps:
        secax = ax.secondary_xaxis('top')
        tick_positions = [global_steps[0]]
        tick_labels = [str(epochs[0])]
        for ep in sorted(epoch_boundaries_steps.keys()):
            idx = int(epoch_boundaries_steps[ep])
            if idx < len(global_steps):
                tick_positions.append(global_steps[idx])
                tick_labels.append(str(ep + 1))
        secax.set_xticks(tick_positions)
        secax.set_xticklabels(tick_labels, fontsize=8)
        secax.set_xlabel('Epoch', fontsize=10)


def _draw_panel_b_decomposition(ax, epoch_data, stage_boundaries):
    """Panel b: Epoch-level loss decomposition as grouped bar or line."""
    if not epoch_data:
        ax.text(0.5, 0.5, 'No epoch data yet', ha='center', va='center',
                transform=ax.transAxes, fontsize=14, color=PALETTE['neutral'])
        ax.text(-0.06, 1.06, 'b', transform=ax.transAxes,
                fontsize=22, fontweight='bold', va='top', ha='right')
        ax.spines['bottom'].set_linewidth(2)
        ax.spines['left'].set_linewidth(2)
        ax.set_ylabel('Loss', fontsize=14)
        ax.set_xlabel('Epoch', fontsize=14)
        return

    epochs = [e['epoch'] for e in epoch_data]
    cls_vals = [e['loss_cls'] for e in epoch_data]
    l1_vals = [e['loss_l1'] for e in epoch_data]
    ciou_vals = [e['loss_ciou'] for e in epoch_data]

    # Stacked area chart
    ax.fill_between(epochs, 0, cls_vals, alpha=0.7, color=PALETTE['loss_cls'], label='Cls Loss')
    ax.fill_between(epochs, cls_vals, np.array(cls_vals) + np.array(l1_vals),
                     alpha=0.7, color=PALETTE['loss_l1'], label='L1 Loss')
    bottom = np.array(cls_vals) + np.array(l1_vals)
    ax.fill_between(epochs, bottom, bottom + np.array(ciou_vals),
                     alpha=0.7, color=PALETTE['loss_ciou'], label='CIoU Loss')
    total = bottom + np.array(ciou_vals)
    ax.plot(epochs, total, color=PALETTE['loss_total'], linewidth=2.5, marker='o',
            markersize=4, label='Total Loss', zorder=5)

    # Stage shading
    _draw_epoch_stage_shading(ax, stage_boundaries)

    ax.set_ylabel('Loss', fontsize=14)
    ax.set_xlabel('Epoch', fontsize=14)
    ax.legend(loc='upper right', fontsize=9, ncol=2)
    ax.text(-0.06, 1.06, 'b', transform=ax.transAxes,
            fontsize=22, fontweight='bold', va='top', ha='right')
    ax.spines['bottom'].set_linewidth(2)
    ax.spines['left'].set_linewidth(2)


